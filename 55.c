/*
~ 对一个二进制数按位取反
原码：最高位为符号位，0为正，1为负
e.g 5: 0000 0101; -5: 1000 0101

反码：正数的反码与原码一致，负数的反码是对原码按位取反，只是符号位不变
e.g 5: 0000 0101; -5: 1111 1010

补码：正数的补码与原码一致，负数的补码对原码按位取反，符号位不变，再加一，符号位可能会变
e.g 5: 0000 0101；-5: 1111 1010 + 1 = 1111 1011
*/

#include<stdio.h>

int main(){
    int a = 5;
    int b = ~a;

    printf("%d\n",a);
    printf("%d\n",b);

    /*
    5: 0000 0101
    ~a: 1111 1010
    -1: 1111 1001
    取反: 1000 0110
    b = -6
    
    */

    return 0;
}