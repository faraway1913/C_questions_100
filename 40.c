/*
将一个数组逆序输出
思路：数组的本质为一块地址空间，[]内的数字代表，这一块地址空间的第几小块，注意初始为第0块，
要将一个数组逆序输出，需要获取到数组有几块小空间，假如有n块：0~n-1，通过循环交换第0块和第n-1块，1和n-2
依次循环，最后输出？
似乎逆序输出的话只需要循环从后往前循环输出每一块空间的内容即可
*/

#include<stdio.h>

int main(){
    int a[] = {1,2,3,4,5,6,7};
    int n;

    n = sizeof(a)/sizeof(int);

    printf("正序输出数组为：\n");
    for(int i=0; i<n; i++){
        printf("%d ",a[i]);
    }

    printf("\n");

    printf("逆序输出数组为：\n");
    for(int i=n-1; i>=0; i--){
        printf("%d ",a[i]);
    }

    return 0;
}